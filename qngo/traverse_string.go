package main

import "fmt"

func main() {
	str := "Hello, 世界"
	n := len(str)
	// byte, each Chinese char use 3byte
	for i := 0; i < n; i++ {
		ch := str[i]
		fmt.Println(i, ch)
	}
	fmt.Println("------------------")
	// rune
	for i, ch := range str {
		fmt.Println(i, ch)
	}

}

